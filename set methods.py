a={'tabbu','arshe','akbari'}
b={'gulshan','shannu','amme'}
print("A:",a)
print("B:",b)
print()
#intersection() Method
ism=a.intersection(b)
print("intersection",ism)
print()
#union() Method
um=a.union(b)
print("union:",um)
print()
#difference()Method
dm=a.difference(b)
print("difference :",dm)
print()
#issubset()Method
issub=a.issubset(b)
print("issubset :",issub)
print()
#superset() Method
issup=a.issuperset(b)
print("issuperset :",issup)
print()